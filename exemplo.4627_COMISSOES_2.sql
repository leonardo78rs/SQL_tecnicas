SELECT SE3.E3_PREFIXO,
       --SE2.E2_PREFIXO,
       SE3.E3_NUM,
       --SE2.E2_NUM,
       SE3.E3_PARCELA,
       --SE2.E2_PARCELA,
       SE3.E3_VEND VENDEDOR,
       SE2.E2_FORNECE FORNECEDOR,
       SE3.E3_COMIS,
       SE2.E2_VALOR,
       SUBSTRING(SE3.E3_EMISSAO, 7, 2) + '/' +
       SUBSTRING(SE3.E3_EMISSAO, 5, 2) + '/' +
       SUBSTRING(SE3.E3_EMISSAO, 1, 4) EMISSAO,
       SE3.E3_DATA,
       SE2.E2_FATPREF,
       SE2.E2_FATURA
  FROM SE3010 SE3, SE2010 SE2
 WHERE SE3.E3_FILIAL = '01'
   AND SE2.E2_FILIAL = '01'
   AND SE3.D_E_L_E_T_ <> '*'
   AND SE2.D_E_L_E_T_ <> '*'
   AND SE3.E3_NUM = SE2.E2_NUM
   AND SE3.E3_COMIS = SE2.E2_VALOR
      --AND SE3.E3_PREFIXO = SE2.E2_PREFIXO
   AND SE3.E3_PARCELA = SE2.E2_PARCELA
      --   AND SE3.E3_NUM = '012242'
   AND SE3.E3_DATA = ''
   AND SE2.E2_FATURA <> ''
 ORDER BY SE3.E3_NUM, SE3.E3_EMISSAO